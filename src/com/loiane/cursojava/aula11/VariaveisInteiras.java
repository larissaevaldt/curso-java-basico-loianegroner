package com.loiane.cursojava.aula11;

public class VariaveisInteiras {

	public static void main(String[] args) {
		//range byte: 1 byte. From -128 to 127.
		byte idade01 = 20;
		//range short: 2 bytes. From -32,768 to 32,767.
		short idade02 = 21;
		//range int: 4 bytes. From -2,147,483,648 to 2,147,483,647.
		int idade03 = 22;
		//range long: 8 bytes, From -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807.
		long idade04 = 23;
		
		System.out.println("Valor Variavel idade01 = " + idade01);
		System.out.println("Valor Variavel idade02 = " + idade02);
		System.out.println("Valor Variavel idade03 = " + idade03);
		System.out.println("Valor Variavel idade04 = " + idade04);
		

	}

}
